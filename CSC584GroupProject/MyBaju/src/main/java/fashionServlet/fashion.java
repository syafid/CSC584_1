package fashionServlet;

public class fashion {
	
	
	    //table design
		private int fashion_id;
		private String fashion_name;
		private String fashion_desc;
		private int price;
		
		public void fashion() {
			
		}

		public int getFashion_id() {
			return fashion_id;
		}

		public void setFashion_id(int fashion_id) {
			this.fashion_id = fashion_id;
		}

		public String getFashion_name() {
			return fashion_name;
		}

		public void setFashion_name(String fashion_name) {
			this.fashion_name = fashion_name;
		}

		public String getFashion_desc() {
			return fashion_desc;
		}

		public void setFashion_desc(String fashion_desc) {
			this.fashion_desc = fashion_desc;
		}

		public int getPrice() {
			return price;
		}

		public void setPrice(int price) {
			this.price = price;
		}

		
		
}

