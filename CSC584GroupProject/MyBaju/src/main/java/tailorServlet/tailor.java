package tailorServlet;

public class tailor {
	private int tailor_id;
	private String tailor_name;
	private String tailor_phone;
	private String job_id;
	private String job_spec;
	
	public void tailor() {
		
	}

	public int getTailor_id() {
		return tailor_id;
	}

	public void setTailor_id(int tailor_id) {
		this.tailor_id = tailor_id;
	}

	public String getTailor_name() {
		return tailor_name;
	}

	public void setTailor_name(String tailor_name) {
		this.tailor_name = tailor_name;
	}

	public String getTailor_phone() {
		return tailor_phone;
	}

	public void setTailor_phone(String tailor_phone) {
		this.tailor_phone = tailor_phone;
	}

	public String getJob_id() {
		return job_id;
	}

	public void setJob_id(String job_id) {
		this.job_id = job_id;
	}

	public String getJob_spec() {
		return job_spec;
	}

	public void setJob_spec(String job_spec) {
		this.job_spec = job_spec;
	}
	
	
}
