package colourServlet;

public class colour {
	private int colour_id;
	private String colour_name;
	private String colour_desc;
	
	public void colour() {
		
	}

	public int getColour_id() {
		return colour_id;
	}

	public void setColour_id(int colour_id) {
		this.colour_id = colour_id;
	}

	public String getColour_name() {
		return colour_name;
	}

	public void setColour_name(String colour_name) {
		this.colour_name = colour_name;
	}

	public String getColour_desc() {
		return colour_desc;
	}

	public void setColour_desc(String colour_desc) {
		this.colour_desc = colour_desc;
	}

	
	
}
